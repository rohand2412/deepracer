def reward_function(params):
    if PARAMS.prev_steps is None or params['steps'] < PARAMS.prev_steps:
        PARAMS.prev_progress = params['progress']
        PARAMS.prev_closest_route_point = None

    # Read input parameters
    vehicle_x = params['x']
    vehicle_y = params['y']

    distances_from_route_points = np.hypot(PARAMS.waypoints[:, 0] - vehicle_x, PARAMS.waypoints[:, 1] - vehicle_y)
    closest_route_point = np.argmin(distances_from_route_points)

    npoints = len(PARAMS.waypoints)

    if PARAMS.prev_closest_route_point is None or (closest_route_point < PARAMS.prev_closest_route_point and abs(closest_route_point - PARAMS.prev_closest_route_point) < 1000):
        diff_wp = 0
    else:
        diff_wp = (closest_route_point - PARAMS.prev_closest_route_point) % npoints

    diff = diff_wp * 100 / npoints

    threshold = 0.25
    constant_slope = 1.75
    base = 2
    shrink = 5

    if diff <= threshold:
        reward = (base ** (shrink * diff)) * diff
    else:
        reward = constant_slope * (diff - threshold) + ((base ** (shrink * threshold)) * threshold)

    if not params['all_wheels_on_track']:
        reward = 0

    PARAMS.prev_progress = params['progress']
    PARAMS.prev_closest_route_point = closest_route_point
    PARAMS.prev_steps = params['steps']
    return float(reward)